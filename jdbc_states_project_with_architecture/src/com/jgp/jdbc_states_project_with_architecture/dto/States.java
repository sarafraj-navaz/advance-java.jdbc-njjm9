package com.jgp.jdbc_states_project_with_architecture.dto;

public class States {

	private int  statesId;
	private String statesName;
	private String statesCapital;
	private int statesDistrict;
	public int getStatesId() {
		return statesId;
	}
	public void setStatesId(int statesId) {
		this.statesId = statesId;
	}
	public String getStatesName() {
		return statesName;
	}
	public void setStatesName(String statesName) {
		this.statesName = statesName;
	}
	public String getStatesCapital() {
		return statesCapital;
	}
	public void setStatesCapital(String statesCapital) {
		this.statesCapital = statesCapital;
	}
	public int getStatesDistrict() {
		return statesDistrict;
	}
	public void setStatesDistrict(int statesDistrict) {
		this.statesDistrict = statesDistrict;
	}
	
}
